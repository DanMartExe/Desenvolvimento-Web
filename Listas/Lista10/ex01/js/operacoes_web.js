export const somar = (numero1, numero2) => {
    return (numero1 + numero2)
}

export const subtrair = (numero1, numero2) => {
    return (numero1 - numero2)
}

export const multiplicar = (numero1, numero2) => {
    return (numero1 * numero2)
}

export const dividir = (numero1, numero2) => {
    return (numero1 / numero2)
}